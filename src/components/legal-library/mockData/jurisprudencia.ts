// Función de búsqueda
export function searchJurisprudencia(query: string): Jurisprudence[] {
  const lowerQuery = query.toLowerCase();
  
  return jurisprudencias.filter(item => 
    item.number.toLowerCase().includes(lowerQuery) ||
    item.materia.toLowerCase().includes(lowerQuery) ||
    item.ponente.toLowerCase().includes(lowerQuery) ||
    item.summary.toLowerCase().includes(lowerQuery) ||
    item.sala.toLowerCase().includes(lowerQuery)
  );
}

// Filtrar por tribunal
export function filterByTribunal(tribunal: Jurisprudence['tribunal']): Jurisprudence[] {
  return jurisprudencias.filter(item => item.tribunal === tribunal);
}

export default {
  jurisprudencias,
  searchJurisprudencia,
  filterByTribunal,
};
